import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.springframework.web.bind.annotation.*;

@RestController
public class BenchmarkTest10578 {

    private static String sharedLastValue = "";
    private static int sharedWriteCount = 0;
    private static final Object SHARED_WRITE_LOCK = new Object();

    @PostMapping("/BenchmarkTest10578")
    public void BenchmarkTest10578(@RequestParam("comment") String commentText, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String commentValue = java.util.Optional.ofNullable(commentText).orElse("");
        synchronized(SHARED_WRITE_LOCK) {
            sharedLastValue = commentValue;
            int seen = sharedWriteCount;
            sharedWriteCount = seen + 1;
        }
    }
}
