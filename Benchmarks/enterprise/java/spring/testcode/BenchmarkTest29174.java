import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.springframework.web.bind.annotation.*;

@RestController
public class BenchmarkTest29174 {

    @GetMapping("/BenchmarkTest29174")
    public void BenchmarkTest29174(HttpServletRequest request, HttpServletResponse response) throws Exception {
        String envValue = java.util.Optional.ofNullable(System.getenv("USER_INPUT")).orElse("");
        java.util.function.Consumer<String> lengthGuard = s -> { if (s.length() > 8192) throw new IllegalArgumentException("input too long"); };
        java.util.function.Function<String, String> normalizer = s -> s.strip().replaceAll("\\s+", " ");
        lengthGuard.accept(envValue);
        String data = normalizer.apply(envValue);
        if (!data.matches("^[\\w\\s<>\\\"'/().;=]+$")) {
            response.sendError(400, "forbidden"); return;
        }
        response.getWriter().print("<div>" + data + "</div>");
    }
}
