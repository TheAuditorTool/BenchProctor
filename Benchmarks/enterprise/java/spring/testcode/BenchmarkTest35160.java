import org.springframework.web.bind.annotation.*;

@RestController
public class BenchmarkTest35160 {

    private static String normalize(String v) { return v.strip(); }

    @PostMapping(path="/BenchmarkTest35160", consumes="multipart/form-data")
    public void BenchmarkTest35160(@RequestPart("multipart_field") String multipartField, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String multipartValue = multipartField != null ? multipartField : "";
        String data = normalize(multipartValue);
        String routeResult = "unknown";
        switch (data) {
            case "retry": routeResult = "retry-handled"; break;
            case "abort": routeResult = "abort-handled"; break;
            default: routeResult = "fallback"; break;
        }
        response.setHeader("X-Route-Result", routeResult);
        response.setContentType("application/json");
        response.getWriter().print("{\"id\":0}");
    }
}
