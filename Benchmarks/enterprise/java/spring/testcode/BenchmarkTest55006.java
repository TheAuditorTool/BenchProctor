public class BenchmarkTest55006 {

    @GetMapping("/BenchmarkTest55006")
    public void BenchmarkTest55006(@RequestHeader("X-Forwarded-For") String xForwardedFor, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String forwardedIp = xForwardedFor != null ? xForwardedFor : "";
        java.util.List<String> tokens = new java.util.ArrayList<>();
        for (String token : forwardedIp.split(",")) { tokens.add(token.trim()); }
        String data = String.join(",", tokens);
        if ("admin".equals(data) || "ROLE_ADMIN".equals(data)) {
            response.getWriter().print("{\"status\":\"ok\"}");
            return;
        }
        response.sendError(403, "forbidden");
    }
}
