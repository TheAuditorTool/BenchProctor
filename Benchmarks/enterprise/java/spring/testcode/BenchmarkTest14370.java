import org.springframework.web.multipart.MultipartFile;

@RestController
public class BenchmarkTest14370 {

    @PostMapping(path="/BenchmarkTest14370", consumes="multipart/form-data")
    public void BenchmarkTest14370(@RequestPart("file") MultipartFile file, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String uploadName = file != null ? file.getOriginalFilename() : "";
        java.util.List<String> tokens = new java.util.ArrayList<>();
        for (String token : uploadName.split(",")) { tokens.add(token.trim()); }
        String data = String.join(",", tokens);
        new java.io.File("/tmp/" + data).createNewFile();
        response.setContentType("application/json");
        response.getWriter().print("{\"id\":0}");
    }
}
