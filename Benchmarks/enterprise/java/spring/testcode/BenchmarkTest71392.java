import java.net.*;
import org.springframework.web.bind.annotation.*;

@RestController
public class BenchmarkTest71392 {

    private static String trimEnds(String v) { return v.trim(); }

    @GetMapping("/BenchmarkTest71392")
    public void BenchmarkTest71392(@RequestHeader("Authorization") String authorization, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String authHeader = authorization != null ? authorization : "";
        String data = trimEnds(authHeader);
        URL url = java.net.URI.create("http://" + data).toURL();
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        try {
            conn.connect();
            conn.getInputStream().close();
        } finally { conn.disconnect(); }
        response.setContentType("application/json");
        response.getWriter().print("{\"id\":0}");
    }
}
