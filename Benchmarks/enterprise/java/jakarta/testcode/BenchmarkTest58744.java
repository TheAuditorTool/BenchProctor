import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.ws.rs.*;
import jakarta.ws.rs.core.Context;
import jakarta.ws.rs.core.MediaType;
import jakarta.ws.rs.core.Response;

@Path("/")
public class BenchmarkTest58744 {

    @GET
    @Path("/BenchmarkTest58744")
    @Produces(MediaType.APPLICATION_JSON)
    public Response BenchmarkTest58744(@QueryParam("id") String id, @Context HttpServletRequest request, @Context HttpServletResponse response) throws Exception {
        String userId = id != null ? id : "";
        java.util.function.Function<String, String> initialFn = s -> s.replace("\t", " ");
        java.util.function.Function<String, String> transformed = initialFn.andThen(String::strip);
        String data = transformed.apply(userId);
        String processed = data.length() > 64 ? data.substring(0, 64) : data;
        return Response.ok("<div>" + processed + "</div>", MediaType.TEXT_HTML).build();
    }
}
