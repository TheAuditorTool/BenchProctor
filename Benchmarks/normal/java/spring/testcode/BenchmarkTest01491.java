import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.springframework.web.bind.annotation.*;
import org.springframework.web.multipart.MultipartFile;

@RestController
public class BenchmarkTest01491 {

    @PostMapping(path="/BenchmarkTest01491", consumes="multipart/form-data")
    public void BenchmarkTest01491(@RequestPart("file") MultipartFile file, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String uploadName = file != null ? file.getOriginalFilename() : "";
        String data = String.join(" ", uploadName.split("\\s+"));
        response.getWriter().print("{\"resource\":\"" + data + "\"}");
    }
}
