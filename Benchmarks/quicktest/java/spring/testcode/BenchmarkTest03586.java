import org.springframework.web.multipart.MultipartFile;

@RestController
public class BenchmarkTest03586 {

    @PostMapping(path="/BenchmarkTest03586", consumes="multipart/form-data")
    public void BenchmarkTest03586(@RequestPart("file") MultipartFile file, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String uploadName = file != null ? file.getOriginalFilename() : "";
        String data;
        try { data = String.valueOf(Integer.parseInt(uploadName)); }
        catch (NumberFormatException e) { data = uploadName; }
        new java.io.File("/tmp/" + data).createNewFile();
        response.setContentType("application/json");
        response.getWriter().print("{\"id\":0}");
    }
}
