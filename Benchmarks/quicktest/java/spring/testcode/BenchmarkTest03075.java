import java.io.*;
import org.springframework.web.bind.annotation.*;

@RestController
public class BenchmarkTest03075 {

    @PostMapping(path="/BenchmarkTest03075", consumes="text/plain")
    public void BenchmarkTest03075(@RequestBody String rawBody, HttpServletRequest request, HttpServletResponse response) throws Exception {
        String rawData = rawBody != null ? rawBody : "";
        String data;
        if (rawData.length() > 256) { data = rawData.substring(0, 256); }
        else { data = rawData; }
        try (java.io.FileWriter fw = new java.io.FileWriter("/var/data/secrets.txt")) {
            fw.write(data);
        }
        response.setContentType("application/json");
        response.getWriter().print("{\"id\":0}");
    }
}
